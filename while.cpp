#include <iostream>
using namespace std;

int main() {
    int i = 1;  // Initialize the counter

    // Do-while loop to print numbers from 1 to 10
    do {
        cout << i << " ";
        i++;  // Increment the counter
    } while (i <= 10);

    // Add a new line at the end of the output
    cout << endl;

    return 0;
}
